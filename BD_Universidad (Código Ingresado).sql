Create Table Tutores 
(
	Id_tutor int Not null,
	Nombre varchar (50) not null,
	Apellido varchar (50) null, 
	Categoría varchar (50) not null,	
	Especialidad varchar (50) not null,
	Fecha_nacimiento date null,
	Constraint PK_Tutores Primary Key (Id_tutor)
);
Create Table Estudiantes 
(
	Id_estudiante int not null,
	Nombre varchar (50) not null,
	Apellido1 varchar (50) null,
	Apellido2 varchar (50) null,
	Fecha_nacimiento date null,
	Constraint PK_Estudiantes Primary Key (Id_estudiante)
);
Create Table Calificaciones 
(
	Id_calificacion int not null,
	Nota_Final int not null,
	Constraint PK_Calificaciones Primary Key (Id_calificacion)
);
Create Table Curso
(
	Id_curso int not null,
	Nombre_curso varchar (100) Null,
	Constraint PK_Curso Primary Key (Id_curso)
);
Create Table Cur_Tut
(
	Id_curso int not null,
	Id_tutor int Not null,
	Constraint FK_Cur_Tut_Cur Foreign key (Id_curso) References Curso(Id_curso),
	Constraint FK_Cur_Tut_Tut Foreign key (Id_tutor) References Tutores(Id_tutor)
);
Create Table Cur_Cal
(
	Id_curso int not null,
	Id_estudiante int not null,
	Id_calificacion int not null,
	Constraint FK_Cur_Cal_Cur Foreign key (Id_curso) References Curso(Id_curso),
	Constraint FK_Cur_Cal_Es Foreign key (Id_estudiante) References Estudiantes(Id_estudiante),
	Constraint FK_Cur_Cal_Cal Foreign key (Id_calificacion) References Calificaciones(Id_calificacion)
);

